#植木算
#小学生のたかはし君は、遠足で林に来ています。遠足を楽しんでいる彼は、木が一直線に並んでいることに気づきました。そして、授業で「植木算」というものを習ったことを思い出しました。彼が授業で習った植木算の問題は「木が四本一直線に並んでいる時、隣り合う木の”間”は何箇所存在するか」というものでその答えは３箇所です。
#今回、遠足中の彼が見ている光景は、その問題のシュチュエーションと似通っていて、隣り合う木の間の数を数えたくなりました。彼は遠足のパンフレットに、一直線に生えている木々の本数が書かれていることに気がつきました。しかし、彼は実際に生えている木の間の数を数える手段を知らないので、本数によってはとても時間がかかるかもしれません。
 
n= int(input("一直線に並んでいる木の本数を入力してください："))
print("隣り合う木の'間'は",n-1,"箇所です")